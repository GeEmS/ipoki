package com.ipoki.xacoveo.javame;

/**
 *
 * @author Xavi
 */
public class Content
{
	private String name;
	private String date;

	public Content(String n, String d)
	{
		name = n;
		date = d;
	}

	public String getName()
	{
		return name;
	}

	public String getDate()
	{
		return date;
	}
}
